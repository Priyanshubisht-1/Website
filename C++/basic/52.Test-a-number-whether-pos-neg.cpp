#include <iostream>
using namespace std;
int main()
{
    int n1 = 11, n2 = -2;
    if (n1 >= 0)
        cout << n1 << " is Positive.  " << endl;
    else
        cout << n1 << " is Negative. " << endl;
    if (n2 >= 0)
        cout << n2 << " is Positive.  " << endl;
    else
        cout << n2 << " is Negative. " << endl;
    return 0;
}