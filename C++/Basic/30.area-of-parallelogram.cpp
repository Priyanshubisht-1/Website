#include <iostream>
using namespace std;
int main()
{
    int b, h;
    float area;
    cout << "Enter the base of Parallelogram : ";
    cin >> b;
    cout << "Enter the height of Parallelogram : ";
    cin >> h;
    area = (b * h);
    cout << "Area of Parallelogram is : " << area << endl;
    return 0;
}